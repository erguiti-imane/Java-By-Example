package data;

class Primitives {
    public static void main(String[] args) {
        int myInt = 2;                      //Integer (whole number)
        float myFloat= 2.7f;                //Floating point number
        char myChar = 'c';                  //Character
        boolean myBoolean = true;           //Boolean
        String myString = "hello there";    //String

        System.out.printf("integer: %d%nfloat: %f%nchar: %c%nboolean: %b%nString: %s",myInt,myFloat,myChar,myBoolean,myString);
    }
}
